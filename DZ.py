import pandas as pd

# Список учеников
imena = ['Анна', 'Борис', 'Виктор', 'Галина', 'Дмитрий', 'Елена', 'Женя', 'Зоя', 'Игорь', 'Ксения']

# Оценки учеников по 5-бальной шкале
ocenki = {
    'Математика': [5, 4, 3, 5, 4, 4, 3, 5, 4, 5],
    'Физика': [4, 5, 3, 4, 5, 4, 3, 5, 4, 5],
    'Химия': [3, 4, 5, 4, 3, 5, 4, 5, 4, 3],
    'Литература': [5, 4, 5, 3, 4, 5, 3, 4, 5, 4],
    'История': [4, 3, 5, 4, 3, 5, 4, 5, 3, 4]
}

# Создание таблицы
df = pd.DataFrame({'Имя': imena, **ocenki})

# Вывод первых строк таблицы
print(f"\nПервые строки таблицы:\n{df.head()}")

# Вычисление среднего балла
print(f"\nСредняя оценка по предметам:\n{df.iloc[:, 1:].mean()}")

# Вычисление медианы
print(f"\nМедианная оценка по предметам:\n{df.iloc[:, 1:].median()}")

# Квартильные значения для математики
Q1 = df['Математика'].quantile(0.25)
Q3 = df['Математика'].quantile(0.75)
IQR = Q3 - Q1

print(f"\nКвартильные значения для математики:\nQ1: {Q1}, Q3: {Q3}, IQR: {IQR}")

# Стандартное отклонение
print(f"\nСтандартное отклонение по предметам:\n{df.iloc[:, 1:].std()}")
